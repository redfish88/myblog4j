package org.ya.blog.orm;

public class Tag {

	public Tag() {
	}

	private int tagId;
	
	private String tag;

	public int getTagId() {
		return tagId;
	}

	public void setTagId(int tagId) {
		this.tagId = tagId;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}
	
}
